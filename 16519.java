public class StapelException extends Exception {

    public StapelException() {
        super();
    }

    public StapelException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public StapelException(String arg0) {
        super(arg0);
    }

    public StapelException(Throwable arg0) {
        super(arg0);
    }
}
