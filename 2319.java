public class JunesWorkExperience {

    private int technical;

    private int financial;

    private int management;

    public int getTechnical() {
        return technical;
    }

    public int getFinancial() {
        return financial;
    }

    public int getManagement() {
        return management;
    }

    public void gainTechnical() {
        technical++;
    }

    public void gainFinancial() {
        financial++;
    }

    public void gainManagement() {
        management++;
    }
}
